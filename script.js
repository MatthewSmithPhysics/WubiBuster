function init()
{
	canvas = document.getElementById("mainCanvas");
	WIDTH = canvas.width;
	HEIGHT = canvas.height;
	ox = Math.floor(WIDTH/2);
	oy = Math.floor(HEIGHT/2);
	res = 0.5*WIDTH;
	ctx = canvas.getContext("2d");
	player = new Player();
}

function generate()
{
	var k = Math.floor(wordbank.length*Math.random());
	character.push(new Character(wordbank[k][0], wordbank[k][1]));
}

function update()
{
	player.update(dt);
	if(!player.shooting)for(var i = 0; i < character.length; i++) character[i].update(dt);
	for(var i = 0; i < character.length; i++)
	{
		if(Math.pow(character[i].x, 2.0) + Math.pow(character[i].y, 2.0) - Math.pow(character[i].r, 2.0) < 0.0)
		{
			currentCharacter.targeted = false;
			character.splice(i, 1);
			pos = 0;
			player.reset();
			lives = lives - 1;
			if(lives == 0) alert("You cannot write Chinese!");
		}
	}
	if(!player.shooting && (toDestroy != -1)) 
	{
		character.splice(toDestroy, 1);
		toDestroy = -1;
	}
}

function keyboardInput(event)
{
	userString = event.key;
	if(pos == 0)
	{
		candidate = []
		for(var i = 0; i < character.length; i++)
		{
			if(userString == character[i].wubi[0]) candidate.push(i);
		}
		if(candidate.length > 0)
		{
			currentIndex = Math.min(candidate);
			currentCharacter = character[currentIndex];
			currentWubi = currentCharacter.wubi;
			currentCharacter.targeted = true;
			player.setAngle(currentCharacter.x, currentCharacter.y);
			pos = pos + 1;
		}
	}else if(userString == currentWubi[pos]) 
	{	
		status = true;
		if(pos == 1) player.fillUp();
		if(pos == 2)
		{
			if(currentWubi.length == 3) 
			{
				player.aim(currentCharacter.x, currentCharacter.y);
				player.pourOut();
			}
			else player.tense = true;
		}
		if(pos == 3) 
		{
			player.aim(currentCharacter.x, currentCharacter.y);
			player.pourOut();
		}
		pos = pos + 1;
		if(pos >= currentWubi.length) 
		{
			player.tense = false;
			pos = 0;
			toDestroy = currentIndex;
			score = score + 1;
		}
	}
}

function draw()
{
	ctx.fillStyle = "#000000";
	ctx.fillRect(0, 0, WIDTH, HEIGHT);
	ctx.font = "20px Arial";
	ctx.fillStyle = "#FFFFFF";
	ctx.fillText("Lives: " + lives + "	 Score: " + score, 50, 30) 
	player.draw(ctx);
	for(var i = 0; i < character.length; i++) character[i].draw(ctx, res, X, Y, scale);
}

function start()
{
	window.setInterval(update, parseInt(1000*dt));
	window.setInterval(draw, parseInt(1000*dt));
	window.setInterval(generate, 10000);
	window.addEventListener("keydown", keyboardInput);
}

function scale(r)
{
	return parseInt(res*r);
}

function X(x)
{
	return parseInt(ox + res*x);
}

function Y(y)
{
	return parseInt(oy - res*y);
}
